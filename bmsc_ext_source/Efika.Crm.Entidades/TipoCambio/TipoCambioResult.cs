using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace Efika.Crm.Entidades.TipoCambio
{
    public class TipoCambioResult
    {

        public const string WSRESULT_TCCONTABLE = "pTCContable";
        public const string WSRESULT_TCCOMPRA = "pTCCompra";
        public const string WSRESULT_TCVENTA = "pTCVenta";
        public const string WSRESULT_RESULTADO = "pResultado";
        public const string WSRESULT_MENSAJE = "pMensaje";


        public TipoCambioResult()
        {}

        public TipoCambioResult(decimal tcContable,decimal tcCompra,decimal tcVenta,int resultado,string msg)
        {
            this.TCContable = tcContable;
            this.TCCompra = tcCompra;
            this.TCVenta = tcVenta;
            this.Resultado  = resultado;
            this.Mensaje  = msg;
        }

        public decimal TCContable
        {get;set;}

        public decimal TCCompra
        {get;set;}

        public decimal TCVenta
        {get;set;}

        public int Resultado
        {get;set;}

        public string Mensaje
        {get;set;}
               

    }
}
